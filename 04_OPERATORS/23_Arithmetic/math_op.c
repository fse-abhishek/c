#include<stdio.h>  // include the standard input/output library for printf
int main ()      // main function: program execution starts here

{
   int a = 5, b = 8;  // declare two integer variables a and b and initialize them

   printf("a=%d,b=%d", a, b);  // print the values of a and b using format specifiers

   int s = 2, l = 4, c = 6, d = 8, e = 10;  // declare multiple integer variables and assign values

   printf("%d,%d,%d,%d", s, l, c, e);  // print the values of s, l, c, and e in sequence
}
